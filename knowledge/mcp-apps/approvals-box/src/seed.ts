export async function seedDatabase(): Promise<void> {
  console.log("Seeding approvals database...");
  console.log(`   Database: ${path.resolve(__dirname, "..", "approvals.db")}`);

  clearAll();
  console.log("  Cleared existing data");

  // Users
  for (const user of USERS) upsertUser(user);
  console.log(`  Inserted ${USERS.length} users`);

  // Approvals + risk assessments
  for (const approval of SEED_APPROVALS) {
    insertApproval(approval);
    const risk = computeRisk({
      id: approval.id,
      type: approval.type,
      amount: approval.amount,
      currency: approval.currency,
      country: approval.country,
      fields: approval.fields,
      attachment_ids: approval.attachment_ids,
      due_at: approval.due_at,
      summary: approval.summary,
    });
    insertRiskAssessment(risk);
    // Update the stored risk_level to match computed value
    getDb()
      .prepare("UPDATE approvals SET risk_level = ? WHERE id = ?")
      .run(risk.risk_level, approval.id);
  }
  console.log(`  Inserted ${SEED_APPROVALS.length} approvals with risk assessments`);

  // Attachments
  for (const att of SEED_ATTACHMENTS) insertAttachment(att);
  for (const att of SEED_ATTACHMENTS) {
    const row = getDb()
      .prepare("SELECT attachment_ids FROM approvals WHERE id = ?")
      .get(att.approval_id) as { attachment_ids: string } | undefined;
    if (row) {
      const ids: string[] = JSON.parse(row.attachment_ids);
      if (!ids.includes(att.id)) {
        ids.push(att.id);
        getDb()
          .prepare("UPDATE approvals SET attachment_ids = ? WHERE id = ?")
          .run(JSON.stringify(ids), att.approval_id);
      }
    }
  }
  console.log(`  Inserted ${SEED_ATTACHMENTS.length} attachments`);

  // Comments
  for (const comment of SEED_COMMENTS) insertComment(comment);
  for (const comment of SEED_COMMENTS) {
    const row = getDb()
      .prepare("SELECT comment_ids FROM approvals WHERE id = ?")
      .get(comment.approval_id) as { comment_ids: string } | undefined;
    if (row) {
      const ids: string[] = JSON.parse(row.comment_ids);
      if (!ids.includes(comment.id)) {
        ids.push(comment.id);
        getDb()
          .prepare("UPDATE approvals SET comment_ids = ? WHERE id = ?")
          .run(JSON.stringify(ids), comment.approval_id);
      }
    }
  }
  console.log(`  Inserted ${SEED_COMMENTS.length} comments`);

  // Audit events
  for (const event of SEED_EVENTS) insertAuditEvent(event);
  for (const event of SEED_EVENTS) {
    const row = getDb()
      .prepare("SELECT history_ids FROM approvals WHERE id = ?")
      .get(event.approval_id) as { history_ids: string } | undefined;
    if (row) {
      const ids: string[] = JSON.parse(row.history_ids);
      if (!ids.includes(event.id)) {
        ids.push(event.id);
        getDb()
          .prepare("UPDATE approvals SET history_ids = ? WHERE id = ?")
          .run(JSON.stringify(ids), event.approval_id);
      }
    }
  }
  console.log(`  Inserted ${SEED_EVENTS.length} audit events`);

  // Print summary
  const db = getDb();
  const counts     = db.prepare("SELECT status,     COUNT(*) as n FROM approvals GROUP BY status")    .all() as { status: string;     n: number }[];
  const riskCounts = db.prepare("SELECT risk_level, COUNT(*) as n FROM approvals GROUP BY risk_level").all() as { risk_level: string; n: number }[];
  const totalAmt   = (db.prepare("SELECT COALESCE(SUM(amount),0) as total FROM approvals WHERE status='pending'").get() as { total: number }).total;

  console.log("\nSeed summary:");
  console.log("  Status:", counts.map((c) => `${c.status}: ${c.n}`).join(", "));
  console.log("  Risk:  ", riskCounts.map((c) => `${c.risk_level}: ${c.n}`).join(", "));
  console.log(`  Total pending amount: $${totalAmt.toLocaleString("en-US", { minimumFractionDigits: 2, maximumFractionDigits: 2 })}`);
  console.log("\nSeed complete.\n");
}

