import '@testing-library/jest-dom';

// Mock ResizeObserver for components that use it
class ResizeObserverMock {
  observe = jest.fn();
  unobserve = jest.fn();
  disconnect = jest.fn();
}

global.ResizeObserver = ResizeObserverMock;

